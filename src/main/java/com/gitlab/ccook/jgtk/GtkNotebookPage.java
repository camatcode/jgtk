package com.gitlab.ccook.jgtk;

import com.gitlab.ccook.util.Option;
import com.sun.jna.Pointer;

/**
 * GtkNotebookPage is an auxiliary object used by GtkNotebook.
 */
@SuppressWarnings("unchecked")
public class GtkNotebookPage extends JGTKObject {
    public GtkNotebookPage(Pointer pointer) {
        super(pointer);
    }

    public Option<GtkWidget> getChild() {
        Option<Pointer> p = new Option<>(library.gtk_notebook_page_get_child(getCReference()));
        if (p.isDefined()) {
            return new Option<>((GtkWidget) JGTKObject.newObjectFromType(p.get(), GtkWidget.class));
        }
        return Option.NONE;
    }
}
